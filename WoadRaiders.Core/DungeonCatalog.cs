namespace WoadRaiders.Core;

/// <summary>
/// The dungeons a raider can choose. Serialized as a byte in the join request,
/// so keep the numbering stable.
/// </summary>
public enum DungeonId : byte
{
    Barrow = 0, // the halls of the Barrow King
    Cairn = 1,  // the ring-tomb (generated by tools/GenerateDungeon.cs)
}

/// <summary>One shipping dungeon: identity, flavor, and where its files live.</summary>
public readonly record struct DungeonInfo(
    DungeonId Id,
    string Name,
    string Tagline,
    string MapFile,   // geometry JSON in the maps directory (canonical copy: WoadRaiders.Client/maps)
    string ScenePath, // authored visuals (res://)
    string MusicKey); // assets/audio/<key>_theme.wav

/// <summary>
/// The dungeon table. Kept in Core so the server (which maps to host), the
/// client (the selection screen, music), and the tests all agree on it.
/// </summary>
public static class DungeonCatalog
{
    public static readonly DungeonInfo[] All =
    {
        new(DungeonId.Barrow, "The Barrow", "Long halls under the mound, where the Barrow King waits.",
            "Barrow.json", "res://maps/Barrow.tscn", "barrow"),
        new(DungeonId.Cairn, "The Cairn", "A ring-tomb of standing stones, its chambers set like a wheel.",
            "Cairn.json", "res://maps/Cairn.tscn", "barrow"),
    };

    public static DungeonInfo Of(DungeonId id) => All[(int)id];

    /// <summary>Coerce an untrusted byte (the wire, a CLI arg) to a known dungeon.</summary>
    public static DungeonId Sanitize(byte id) => id < All.Length ? (DungeonId)id : DungeonId.Barrow;

    /// <summary>The catalog entry rendering this scene, if any (drives per-dungeon music).</summary>
    public static DungeonInfo? ForScene(string scenePath)
    {
        foreach (var info in All)
            if (info.ScenePath == scenePath)
                return info;
        return null;
    }
}
